        <h4>Du côté WordPress</h4>

        <p>Relativement facile d’utilisation, il permet de convevoir un site web simplement, et rapidement Mais lorsqu’on regarde la chose plus en détails, il comporte pas mal d’inconvénients.
        </p>

        <p>LES +</p>

        <p>WordPress est un CMS multi-tâches. WordPress propose un grand nombre de plugins et de widgets à ajouter pour personnaliser son site. WordPress est un CMS open-source. Autrement dit, il est possible de modifier le code du logiciel.</p>

        <p>LES -</p>

        <p>Il est très simple pour des pirates de s’insérer dans une faille de sécurité et de prendre la main sur votre site. <br>
        Le nombre de plugins WordPress : les plugins ne sont pas forcément compatibles entre eux, ce qui peut entraîner des ralentissements voire des bugs.</p>

        <h4>Du côté des développeurs</h4>

        <p>LES +</p>

        <p>Liberté d’action que ça soit au niveau du graphisme ou des outils on-site. Vous pouvez donc laisser le champ libre à votre imagination pour créer des fonctionnalités vraiment adaptées à votre activité.<br> 
        Plus sécurisé <br> 
        Le développeur web se charge aussi de prévenir tout bug de votre site. De quoi vous tranquilliser l’esprit !</p>

        <p>LES -</p>

        <p>Le seul inconvénient ? Le coût initial, car à la longue un site codé en ‘dur‘ seras plus stable dans le temps, nécessitera moins de maintenance, diminuant ainsi les frais sur le long terme.</p>

        <h4>conclusion</h4>

        <p><strong>Five Forward à donc opté pour une démarche qualité optimale visant à satisfaire le client à la livraison et surtout, dans la durée en concevant un site WEB de A à Z complètement personnalisé et unique suivant vos demandes. </strong></p>

