        <h3 class="text-center">Création du cachier des charges</h3>

            <p><strong>Le cahier des charges décrit précisément les besoins auxquels le prestataire doit répondre,
                    et organise la relation entre les différents acteurs tout au long d'un projet</strong></p>

            <img class="mw-100" src="../images/cahier_des_charges2.jpg" alt="Illustration du cahier des charges">
            <p class="text-center"><small>Auteur : Jean No' Brabant // Mis en ligne : 3 juillet 2019 // Temps de lecture : 5 minutes</small></p>

            <p>Ce document prend des formes variables selon le type d’activité (production ou service récurrent,
                projet ponctuel…), selon le domaine d’activité principal concerné et selon la culture d’entreprise.
                Cependant le cahier des charges sert à formaliser les besoins et à les expliquer aux différents
                acteurs pour s’assurer que tout le monde soit d’accord. Il permet notamment de cadrer les missions
                des acteurs impliqués, dont celles du chef de projet .</p>

            <p>Il sert ensuite à sélectionner le prestataire ou soumissionnaire, et à organiser la relation tout au
                long du projet. Il est considéré comme un référentiel partagé par le prestataire et l’équipe
                interne, et décliné dans les documents contractuels. Vers l’externe, c'est en outre un outil
                fondamental de communication du directeur de projet et/ou du chef de projet.</p>

            <p>C'est un document contractuel entre le client et le prestataire, mais il ne constitue pas à lui seul
                le contrat commercial. Il permet aussi de remplir l’obligation générale d'information du du
                prestataire visà-vis de son client (code de la consommation, Livre Ier, titre Ier, chapitre Ier).
            </p>

        <h4>Partie technique d'un cahier des charges :</h4>

        <p>La partie technique d’un cahier des charges doit se limiter à énumérer les contraintes techniques
            avérées (on parle souvent d'exigences). Les exigences sont organisées en thèmes, puis elles sont
            numérotées. Cette organisation, prônée par la gestion de projet, facilite le contrôle de conformité
            des réponses à appel d'offres sur cahier des charges. En contrôle de projet, et notamment lors des
            revues de projet, le contrôle de conformité se réalise souvent sous forme de matrice de conformité.
        </p>

        <h4>Les contraintes de base sont :</h4>

        <p>Economiques (les contraintes monétaires comme le budget de fonctionnement), environnementales (le
            caractère recyclable du produit, etc.), industrielles (par exemple, il doit être fabriqué au
            Canada), matérielles (par exemple, il doit spécifier les morceaux qui peuvent être remplacés, comme
            des piles, il doit réciser le recours à tel ou tel serveur d’applications, la plate-forme de
            déploiement).</p>

        <p>L’erreur la plus courante est de confondre préférences et contraintes. Ceci cause des
            incompréhensions et des remises en cause qui peuvent affecter la réalisation du projet. Pour
            remédier à ce problème, il faut inclure tous les acteurs du projet pour définir les besoins</p>

        <p>Les contraintes doivent être interprétées de façon que leur lecture soit l'expression des besoins
            techniques commandés et livrés. Leur interprétation doit être le résultat d'une analyse quantitative
            et qualitative du projet, dans son ensemble.</p>

        <h4>Structure d'un Cahier des Charges :</h4>

        <ol>
            <li>Présentation du projet
                <ol>
                    <li>Contexte
                        <p> Environnement dans lequel s'inscrit le projet (stratégie, enjeux, domaine, etc.)</p>
                    </li>

                    <li>Objectifs
                        <p> Résultats que le projet doit atteindre</p>
                    </li>
                    <li>Description de l'existant
                        <p> Environnement logiciel et matériel du logiciel. Système existant, le cas échéant</p>
                    </li>

                    <li>Critères d'acceptabilité du produit
                        <p> Procédure de validation. Critères d'acceptation.</p>
                    </li>
                </ol>
            </li>
            <li>Expression des besoins.
                <ol>
                    <li>Besoin fonctionnels
                        <p> Fonctions (ou opérations, ou encore transformations) que le logiciel doit réaliser. Les spécications fonctionnelles peuvent être classées par importance.</p>
                    </li>
                    <li>Besoins non fonctionnels
                        <p>Les spécications non fonctionnelles sont toutes les spécications qui n'expriment pas une fonction du logiciel (contraintes de performance, système d'exploitation cible...).</p>
                    </li>
                </ol>
            </li>
            <li>Contraintes
                <ol>
                    <li>Coûts
                        <p> Budget alloué au projet. Moyens matériels et logiciels mis à disposition.</p>
                    </li>
                </ol>
            </li>
        </ol>
